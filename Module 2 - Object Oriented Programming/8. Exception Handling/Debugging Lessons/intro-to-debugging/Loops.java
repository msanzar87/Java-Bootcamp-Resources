public class Loops {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print("*");
            }System.out.println("\n");
        }
//when i equals 0 j will also equal 0 and print the star.
    }
}
